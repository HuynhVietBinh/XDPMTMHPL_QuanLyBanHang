    public void showDonDatHang() {
        hashMapHieu.put(3, "Apple");
        hashMapHieu.put(4, "HP");
        hashMapHieu.put(5, "Dell");
        hashMapHieu.put(6, "SamSung");
        hashMapHieu.put(7, "Asus");
        
        hashMapLoai.put(12, "Ipads");
        hashMapLoai.put(14, "Laptop");
        hashMapLoai.put(15, "Mobiles");
        hashMapLoai.put(16, "Camera");
        hashMapLoai.put(17, "Touchpad");
        
        nhapKhoList = BLL.KhoBLL.loadDataNhapKho();
        dtmNhapKho.setRowCount(0);
        nhapKhoList.forEach((KhoDTO) -> {
            String hieu = BLL.KhoBLL.findHieuByID(KhoDTO.getHieu());
            String loai = BLL.KhoBLL.findLoaiByID(KhoDTO.getLoai());
            dtmNhapKho.addRow(new Object[]{KhoDTO.getDonhang_id(), KhoDTO.getProduct_id(), KhoDTO.getSoluong(),hieu,
                loai, KhoDTO.getTenhang(), KhoDTO.getGianhap(), KhoDTO.getLydonhap(), KhoDTO.getNgaynhap()});
//                panel.datHangList.add(DatHangDTO);
//                arrayState.add(getState);
//                dtmDuyet.addRow(new Object[]{"Duyệt"});

        });
        
    }
    public void TimKiem() {
        TableRowSorter<TableModel> rowSorter = new TableRowSorter<>(tblNhapKho.getModel());
        tblNhapKho.setRowSorter(rowSorter);
        txtTim.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent de) {
                String text = txtTim.getText();
                if (text.trim().length() == 0) {
                    rowSorter.setRowFilter(null);
                } else {
                    rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + text));
                }
            }

            @Override
            public void removeUpdate(DocumentEvent de) {
                String text = txtTim.getText();
                if (text.length() == 0) {
                    rowSorter.setRowFilter(null);
                } else {
                    rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + text));
                }
            }

            @Override
            public void changedUpdate(DocumentEvent de) {
                throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
            }
        });
    }
