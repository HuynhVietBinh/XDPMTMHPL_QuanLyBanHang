    private void btnNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNhapActionPerformed
        // TODO add your handling code here:
        int masp = Integer.parseInt(txtMasp.getText());
        int sl = Integer.parseInt(txtSl.getText());
        Set<Integer> keyHieu = hashMapHieu.keySet();
        Set<Integer> keyLoai = hashMapLoai.keySet();
        
        String hieu = cbHieu.getSelectedItem().toString();
        String loai = cbLoai.getSelectedItem().toString();
        
        int mahieu = 0;
        int maloai = 0;
        for(Integer key : keyHieu){
            if(hieu.equals(hashMapHieu.get(key))){
                mahieu = key;
            }
        }
        for(Integer key : keyLoai){
            if(loai.equals(hashMapLoai.get(key))){
                maloai = key;
            }
        }
        
        int gia = Integer.parseInt(txtMasp.getText());
        KhoDTO kho = new KhoDTO(masp, sl, mahieu, maloai, txtTen.getText(), gia, txtLydo.getText());
        BLL.KhoBLL.addKho(kho);
        
        JOptionPane.showMessageDialog(null, "Thêm thành công.");
        String keyword = loai + "," + hieu;
        SanPhamDTO addsp = new SanPhamDTO(maloai, mahieu, txtTen.getText(), Integer.parseInt(txtGia.getText()), loai, Integer.parseInt(txtSl.getText()), "No image", keyword);
        DAL.StorageDAL.addToStorage(addsp);
        showDonDatHang();
    }//GEN-LAST:event_btnNhapActionPerformed

